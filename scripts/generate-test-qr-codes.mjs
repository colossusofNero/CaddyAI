/**
 * generate-test-qr-codes.mjs
 *
 * Generates a small batch of test QR codes that point at the redeem landing
 * page. Useful for sanity-checking the scan flow without provisioning real
 * Firestore-backed codes.
 *
 * Usage:
 *   node scripts/generate-test-qr-codes.mjs
 *   node scripts/generate-test-qr-codes.mjs --count 20
 *   node scripts/generate-test-qr-codes.mjs --base http://localhost:3010
 */

import QRCode from 'qrcode';
import crypto from 'crypto';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// --- args ---------------------------------------------------------------
const argv = process.argv.slice(2);
function arg(name, fallback) {
  const i = argv.indexOf(`--${name}`);
  return i >= 0 ? argv[i + 1] : fallback;
}
const COUNT = parseInt(arg('count', '10'), 10);
const BASE_URL = arg('base', 'https://copperlinegolf.com').replace(/\/+$/, '');
const OUT_DIR = path.resolve(__dirname, 'output', 'test-qr-codes');

// --- code generation (mirrors scripts/generate-promo-codes.js) ---------
const CHARS = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
function generateCode() {
  let result = '';
  const bytes = crypto.randomBytes(8);
  for (let i = 0; i < 8; i++) result += CHARS[bytes[i] % CHARS.length];
  return `COPPER-PRO-${result}`;
}

// --- main ---------------------------------------------------------------
fs.mkdirSync(OUT_DIR, { recursive: true });

const rows = [];
const seen = new Set();

for (let i = 0; i < COUNT; i++) {
  let code;
  do { code = generateCode(); } while (seen.has(code));
  seen.add(code);

  const url = `${BASE_URL}/redeem?code=${code}`;
  const file = path.join(OUT_DIR, `qr-${code}.png`);

  await QRCode.toFile(file, url, {
    errorCorrectionLevel: 'M',
    margin: 2,
    width: 600,
    color: { dark: '#000000', light: '#FFFFFF' },
  });

  rows.push({ code, url, file: path.relative(OUT_DIR, file) });
  console.log(`  ${code}  ->  ${path.basename(file)}`);
}

// CSV manifest
const csvPath = path.join(OUT_DIR, 'test_qr_codes.csv');
const csv = ['Code,URL,File', ...rows.map(r => `${r.code},${r.url},${r.file}`)].join('\n');
fs.writeFileSync(csvPath, csv + '\n');

// README
const readmePath = path.join(OUT_DIR, 'README.md');
const readme = `# Test QR Codes

Generated by \`scripts/generate-test-qr-codes.mjs\` for manual testing of the
\`/redeem\` landing-page scan flow.

**Important:** these codes are NOT provisioned in Firestore — scanning them
will reach the landing page successfully, but validation will fail with
"Invalid promo code". To test a full successful redemption, use a real code
from \`scripts/generate-promo-codes.js\`.

Base URL: \`${BASE_URL}\`
Count: ${COUNT}

| Code | URL | File |
|------|-----|------|
${rows.map(r => `| \`${r.code}\` | ${r.url} | ${r.file} |`).join('\n')}

## Regenerate

\`\`\`
node scripts/generate-test-qr-codes.mjs --count 10
node scripts/generate-test-qr-codes.mjs --count 5 --base http://localhost:3010
\`\`\`
`;
fs.writeFileSync(readmePath, readme);

console.log(`\nWrote ${rows.length} QR codes to ${OUT_DIR}`);
console.log(`Manifest: ${path.relative(process.cwd(), csvPath)}`);
console.log(`Readme:   ${path.relative(process.cwd(), readmePath)}`);
